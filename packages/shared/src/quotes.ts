/** Normalize a seed to a stable quote index (0..N-1) */
export function normalizeQuoteSeed(seed: number): number {
  return seed % quotes.length;
}

/** Get a quote by index (wraps around if index exceeds array length) */
export function getQuoteByIndex(index: number): Quote {
  return quotes[index % quotes.length];
}

/** Get the author of a quote by index */
export function getQuoteAuthor(index: number): string {
  return quotes[index % quotes.length].author;
}

/** Get a quote split into words by index */
export function getQuoteWords(index: number): string[] {
  return getQuoteByIndex(index).text.split(" ").filter((w) => w.length > 0);
}

/** Split a quote into words by spaces */
export function quoteToWords(quote: string): string[] {
  return quote.split(" ");
}
